package day2;

public class Loop {
    public static void main(String[] args) {

        // 반복문 > 첫번째 벽

        // while, for

        // while

        int i = 0;

        while (i < 3) {
            System.out.println("안녕");
            i++;
        }

        // for
        for (int j = 0; j < 3; j++) {
            System.out.println("안녕");
        }

        for (int a = 1; a <= 100; a++) {
            System.out.println(a);
        }

   }
}
